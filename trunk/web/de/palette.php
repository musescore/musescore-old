<h4><a href="idx.php">MuseScore</a> -- <a href="manual.php">Dokumentation</a> -- <a href="reference.php">Index</a> -- Paletten</h4>

    Paletten enthalten Symbole, die via Drag&amp;Drop auf Elemente der
    Partitur gezogen werden können.
    <br/>
<br/>

    Ein Doppelclick auf ein Palettenelement wirkt, als ob dieses
    Element auf alle selektierten Elemente der Partitur gezogen würde.
    Damit können z.B. auf einfache Weise gleiche mehrere Noten mit einem
    Tenuto-Zeichen versehen werden:
    <ul>
  <li>
Noten selektieren    </li>
  <li>
Doppelclick auf das Tenuto Symbol der Attribute-Palette    </li>
  </ul>
