      Eine Tonart kann gesetzt oder verändert werden, indem ein Tonartsymbol
      von der Tonartpalette auf einen Takt oder ein Tonartsymbol der Partitur
      gezogen wird.
      <br>

  &lt;<b>F9</b>&gt; zeigt das Palettenfenster.<br>
  <br>
  <b>Verändern</b><br>
  Ziehe ein Tonartsymbol von der Tonartpalette auf ein Tonartsymbol
  der Partitur. Mit &lt;<b>Shift+leftMouseButton+Drag</b>&gt; kann auch
  ein Tonartsymbol der Partitur gezogen werden.
  <br>
  <b>Zufügen</b><br>
  Ziehe ein Tonartsymbol fon der Tonartpalette auf einen leeren Teil
  eines Taktes. Dies erzeugt einen Tonartwechsel am Anfang des Taktes.
  <br>
  <br>
  <b>Entfernen</b><br>
  Wähle ein Tonartsymbol aus und drücke dann &lt;<b>Del</b>&gt;.
