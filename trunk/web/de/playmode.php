<h4><a href="idx.php">MuseScore</a> -- <a href="manual.php">Dokumentation</a> -- <a href="reference.php">Index</a> -- Wiedergabemode</h4>
<i>MuseScore</i> besitzt einen integrierten Sequenzer sowie einen
    Synthesizer, um die Partitur wiedergeben zu können.
    <br/>

    Durch drücken des &lt;<b>Play</b>&gt; Buttons wird der &lt;<i>Playmodus</i>&gt;
    gestartet. Im &lt;<i>Playmodus</i>&gt; sind die folgenden Kommandos verfügbar:
    <br/>
<br/>
<table cellspacing="0" cellpadding="0">
  <tr>
    <td>
&lt;<b>Leertaste</b>&gt;      </td>
    <td>
Stop/Play Taster      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Links</b>&gt;      </td>
    <td>
 zum vorherigen Akkord      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Rechts</b>&gt;      </td>
    <td>
 zum nächsten Akkord      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Strg+Links</b>&gt;      </td>
    <td>
 zum vorherigen Takt      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Strg+Rechts</b>&gt;      </td>
    <td>
 zum nächsten Takt      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Pos1</b>&gt;      </td>
    <td>
Zurück zum Anfang      </td>
    </tr>
  <tr>
    <td>
&lt;<b>F11</b>&gt;      </td>
    <td>
zeigt das Playpanel      </td>
    </tr>
  </table>
<br/>
<br/>

    Der voreingestellte Soundfont kann nur ein Instrument (Piano) spielen und
    klingt auch nicht so toll, ist dafür aber sehr klein. Besser klingende Soundfonts
    können unter Bearbeiten/Einstellungen/Midi konfiguriert werden.
