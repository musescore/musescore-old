<h4><a href="idx.php">MuseScore</a> -- <a href="manual.php">Documentation</a> -- <a href="reference.php">Index</a> -- Grace notes</h4>


    A short grace note (Acciaccatura) is painted as a small note with a stroke
    through the stem. Its placed before the normal sized main note.
    A long grace note (Appoggiatura) has no stroke.
    <br/>
<br/>

    Create a grace note by dragging a grace note symbol from the notes palette
    to a main note.
