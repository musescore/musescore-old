<h4><a href="idx.php">MuseScore</a> -- <a href="manual.php">Documentation</a> -- <a href="reference.php">Index</a> -- Play Mode</h4>
<i>MuseScore</i> has an integrated sequencer and synthesizer to play
    your score.<br/>

    By pressing the &lt;<b>Play</b>&gt; button you enter &lt;<i>Play</i>&gt;
    mode. In &lt;<i>Play</i>&gt; mode the following commands are
    available:<br/>
<br/>
<table cellspacing="0" cellpadding="0">
  <tr>
    <td>
&lt;<b>Space</b>&gt;      </td>
    <td>
toggle stop/play      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Left</b>&gt;      </td>
    <td>
 seek to previous chord      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Right</b>&gt;      </td>
    <td>
 seek to next chord      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Ctrl+Left</b>&gt;      </td>
    <td>
 seek to previous measure      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Ctrl+Right</b>&gt;      </td>
    <td>
 seek to next measure      </td>
    </tr>
  <tr>
    <td>
&lt;<b>Home</b>&gt;      </td>
    <td>
rewind start of score      </td>
    </tr>
  <tr>
    <td>
&lt;<b>F11</b>&gt;      </td>
    <td>
toggle show play panel      </td>
    </tr>
  </table>
<br/>
<br/>

    If you want to play other instruments than piano you have to
    change the <i>MuseScore</i> integrated <i>SoundFont</i> with a more
    capable one in the <i>Preferences</i> menu.

