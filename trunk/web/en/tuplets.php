To create a triplet first create a note with a duration of the whole
triplet. Then select the note and press &lt;<b>Ctrl+3</b>&gt; to change
it to a triplet. Similarly a &lt;<b>Ctrl+5</b>&gt; changes to note
to a quintuplet.

