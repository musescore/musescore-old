      Keys are created or changed by dragging a key symbol from the
      key palette to a measure or another key.      <br/>

      &lt;<b>F9</b>&gt; toggles the palettes window.      <br/>
      <br/>
<b>Change</b>      <br/>

      Drag a key from the palette onto a key in the score. You can
      also drag a key from the score to another key of the score
      by using &lt;<b>Shift+leftMouseButton+Drag</b>&gt;.      <br/>
      <br/>
<b>Add</b>      <br/>

      Drag a key from the palette onto an empty part of a measure.
      This creates a key at the beginning of the measure.      <br/>
      <br/>
<b>Remove</b>      <br/>

      Select a key and press &lt;<b>Del</b>&gt;.
