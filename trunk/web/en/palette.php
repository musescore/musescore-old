   -- Palettes</h4>

Your can drag&drop palette symbols onto score elements.
<br>
<br>
A doubleclick on a symbol in a palette is equivalent to
drag&drop this symbol to all selected score elements.
For example you can easily put a tenuto to several notes at once:
<ul>
      <li>select notes</li>
      <li>double click on the tenuto symbol in the attributes palette</li>
</ul>

