//---------------------------------------------------------
//   fontId2Font
//---------------------------------------------------------

Font fontId2font(int fontId)
      {
      Font _font;
      //
      // font is rendered with a physical resolution of PDPI
      //    and logical resolution of DPI
      //
      // rastral size is 20pt = 20/72 inch
      //
      qreal size = 20.0 * DPI / PPI;
      if (fontId == 0) {
            _font.setFamily("MScore");
//            _font.setStyleStrategy(Font::NoFontMerging);
            }
      else if (fontId == 1)
            _font.setFamily("MScore1");
      else if (fontId == 2) {
            _font.setFamily("FreeSerif");
//            _font.setStyleStrategy(Font::NoFontMerging);
            size = 8 * DPI / PPI;
            }
      else if (fontId == 3) {
            _font.setFamily("Gonville-20");
            }
      else {
            printf("illegal font id %d\n", fontId);
            abort();
            }
//      _font.setStyleStrategy(Font::NoFontMerging);
      _font.setSize(size);
      return _font;
      }

//---------------------------------------------------------
//   Sym
//---------------------------------------------------------

Sym::Sym(const char* name, int c, int fid, qreal ax, qreal ay)
   : _code(c), fontId(fid), _name(name), _font(fontId2font(fid)), _attach(ax * DPI/PPI, ay * DPI/PPI)
      {
      FontMetricsF fm(_font);
      w     = fm.width(_code);
      _bbox = fm.boundingRect(_code);
      _idx   = 0;
      }

Sym::Sym(const char* name, int c, int i, const QPointF& a, const QRectF& b)
   : _code(c), _idx(i), fontId(0), _name(name), _font(fontId2font(fontId))
      {
      qreal s = DPI/PPI;
      _bbox.setRect(b.x() * s, b.y() * s, b.width() * s, b.height() * s);
      _attach = a * s;
      w = _bbox.width();
      _idx = i;
      }

//---------------------------------------------------------
//   bbox
//---------------------------------------------------------

const QRectF Sym::bbox(qreal mag) const
      {
      return QRectF(_bbox.x() * mag, _bbox.y() * mag, _bbox.width() * mag, _bbox.height() * mag);
      }

//---------------------------------------------------------
//   draw
//---------------------------------------------------------

void Sym::draw(Painter* painter, qreal mag, qreal x, qreal y) const
      {
      qreal imag = 1.0 / mag;
      painter->scale(mag, mag);
      painter->drawSymbol(x * imag, y * imag, _idx);
      painter->scale(imag, imag);
      }

//---------------------------------------------------------
//   draw
//---------------------------------------------------------

void Sym::draw(Painter* painter, qreal mag, qreal x, qreal y, int n) const
      {
      qreal imag = 1.0 / mag;
      painter->scale(mag, mag);
      painter->drawText(_font, QPointF(x * imag, y * imag), QString(n, _code));
      painter->scale(imag, imag);
      }

